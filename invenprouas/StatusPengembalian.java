    private void tampilkanDataStatus() {
        DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Nama Penyewa");
    model.addColumn("Kode Barang");
    model.addColumn("Nama Barang");
    model.addColumn("Status");

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/invenpro", "root", "");
        Statement stmt = conn.createStatement();

        // JOIN tabel penyewa dan barang
        String query = "SELECT p.nama, p.kode_barang, b.nama_barang, p.status " +
                       "FROM penyewa p JOIN barang b ON p.kode_barang = b.kode_barang";

        ResultSet rs = stmt.executeQuery(query);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nama"),
                rs.getString("kode_barang"),
                rs.getString("nama_barang"),
                rs.getString("status")
            });
        }

        tblStatus.setModel(model);

        rs.close();
        stmt.close();
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan data status: " + e.getMessage());
    }
    }
