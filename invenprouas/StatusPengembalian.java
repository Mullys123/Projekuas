    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String namaPenyewa = txtNamaPenyewa.getText();
    String status = "";

    if (rbtnSelesai.isSelected()) {
        status = "Selesai";
    } else if (rbtnBelumKembali.isSelected()) {
        status = "Belum kembali";
    }

    if (namaPenyewa.isEmpty() || status.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harap isi Nama Penyewa dan pilih Status!");
        return;
    }

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/invenpro", "root", "");

        // Update status penyewa berdasarkan nama
        String sqlUpdate = "UPDATE penyewa SET status = ? WHERE nama = ?";
        PreparedStatement pst = conn.prepareStatement(sqlUpdate);
        pst.setString(1, status);
        pst.setString(2, namaPenyewa);

        int updated = pst.executeUpdate();

        if (updated > 0) {
            JOptionPane.showMessageDialog(this, "Status berhasil diperbarui!");
            tampilkanDataStatus(); // refresh table
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan untuk nama: " + namaPenyewa);
        }

        pst.close();
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

